    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        if (report == null){
            report = new ChronicConditionsReport();
            report.setPID(Integer.parseInt(lblPID.getText()));
            report.setHeartDisease(ckHeartDis.isSelected());
            report.setStroke(ckStroke.isSelected());
            report.setCancer(ckCancer.isSelected());
            report.setDiabetes(ckDiabetes.isSelected());
            report.setObesity(ckObesity.isSelected());
            report.setArthritis(ckArthritis.isSelected());
            report.setChronicConditionsCol(txtOther.getText());
        }else{
            report.setHeartDisease(ckHeartDis.isSelected());
            report.setStroke(ckStroke.isSelected());
            report.setCancer(ckCancer.isSelected());
            report.setDiabetes(ckDiabetes.isSelected());
            report.setObesity(ckObesity.isSelected());
            report.setArthritis(ckArthritis.isSelected());
            report.setChronicConditionsCol(txtOther.getText());
        }
        parent.setEnabled(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnDoneActionPerformed

