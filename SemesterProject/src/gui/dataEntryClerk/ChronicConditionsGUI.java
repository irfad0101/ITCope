    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if (report == null){
            ckHeartDis.setSelected(false);
            ckStroke.setSelected(false);
            ckCancer.setSelected(false);
            ckDiabetes.setSelected(false);
            ckObesity.setSelected(false);
            ckArthritis.setSelected(false);
            txtOther.setText("");
        }else{
            ckHeartDis.setSelected(report.isHeartDisease());
            ckStroke.setSelected(report.isStroke());
            ckCancer.setSelected(report.isCancer());
            ckDiabetes.setSelected(report.isDiabetes());
            ckObesity.setSelected(report.isObesity());
            ckArthritis.setSelected(report.isArthritis());
            txtOther.setText(report.getChronicConditionsCol());
        }
        parent.setEnabled(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnCancelActionPerformed

